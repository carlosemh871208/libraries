/*****************************Macros definitions*******************************/
#define GPIOMASK        ((__IO uint32_t)0x00000100)
#define ISFMASK         ((__IO uint32_t)0x01000000)
#define ADCMASK         ((__IO uint32_t)0x00000000)
#define CLEARIF         ((__IO uint32_t)0x00004000)
/******************************************************************************/
/***************************Constants definitions******************************/

/******************************************************************************/
/*****************************Exported Variables*******************************/

