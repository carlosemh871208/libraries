void NVIC_init_IRQs(IRQn_Type IRQn,uint8_t Priority)
{
	S32_NVIC->ICPR[1] = Input<<(IRQn%32U);
	S32_NVIC->ISER[1] = Input<<(IRQn%32U);
	S32_NVIC->IP[IRQn] = Priority;
}
